    public boolean validarDatos() {
        // Comprobar que los campos de texto no están vacíos
        if (txtMatricula.getText().isEmpty() || txtMarca.getText().isEmpty() || txtModelo.getText().isEmpty() || txtYearPlate.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        // Comprobar que los campos de texto que deben contener números contienen realmente números
        try {
            if (txtKm.getText().isEmpty()) {
                txtKm.setText("0");
            } else {
                Integer.parseInt(txtKm.getText());
            }
            Integer.parseInt(txtYearPlate.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Los campos Kilómetros y Año de Matriculación deben ser números", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        // Comprobar que la matrícula no está ya en uso
        String matricula = txtMatricula.getText();
        ArrayList<Coche> coches = CocheSingleton.getInstance().getCoches();
        for (Coche coche : coches) {
            if (coche.getMatricula().equals(matricula)) {
                JOptionPane.showMessageDialog(this, "La matrícula ya está en uso", "Error", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        }
        
        return true;
    }

