    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (validarDatos()) {
            coche.setMatricula(txtMatricula.getText());
            coche.setMarca(txtMarca.getText());
            coche.setModelo(txtModelo.getText());
            coche.setKilometros(Integer.parseInt(txtKm.getText()));
            coche.setAnoMatriculacion(Integer.parseInt(txtYearPlate.getText()));
            
            coches.add(coche);
            CocheSingleton.getInstance().addCoche(coche);
            JOptionPane.showMessageDialog(this, "Coche agregado con éxito", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        }
        
        txtMatricula.setText("");
        txtMarca.setText("");
        txtKm.setText("");
        txtModelo.setText("");
        txtYearPlate.setText("");
    }//GEN-LAST:event_btnAddActionPerformed

